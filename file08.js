// booleans and comparison operator :
// booleans : true or false :

let num1 = "7";
let num2 = 7;
console.log(num1 <= num2);

// == vs === :

console.log(num1 == num2); // checks only value.
console.log(num1 === num2); // checks value as well as data type also.

// != vs !== :

console.log(num1 != num2);
console.log(num1  !== num2);

